# dict 字典
'''
数字类型是 非容器类型，不能转换为字典
字符串无法转换为字典
列表可以转化为字典，要求是一个二级列表，并且每个二级元素只有两个值
元组可以转化为字典，要求是一个二级列表，并且每个二级元素只有两个值
'''
# n='123'
# n={'a','b',1,'c'}
# n=(1,2,3)
# n={'name':'zhangsan','age':20}
# n=[1,2,3] #一阶列表无法转为字典
n = [[1, 2], [11, 22], ['a', 54]]  # [[1, 2], [11, 22], ['a', 54]] <class 'list'> {1: 2, 11: 22, 'a': 54} <class 'dict'>
# n=((1,2),(11,22))#((1, 2), (11, 22)) <class 'tuple'> {1: 2, 11: 22} <class 'dict'>
res = dict(n)
print(n, type(n), res, type(res))
