# 容器类型数据转换

'''
字符串 转换为列表时 会把字符串中的每一个字符当做列表的元素
数字类型是 非容器类型，不能转换为列表
集合可以转换为列表类型 但无序
元组可以转换为列表类型
字典可以转换为列表类型，但只保留了字典中的键
'''

# lsit 列表
# n='123'
# n={'a','b',1,'c'}
# n=(1,2,3)
n = {'name': 'zhangsan', 'age': 20}
res = list(n)
print(n, type(n), res, type(res))

# tuple 元组
'''
数字类型是 非容器类型，不能转换为元组
其他容器类型的数据进行转化时，和列表一样
'''
# n='123'
# n={'a','b',1,'c'}
# n=(1,2,3)
n = {'name': 'zhangsan', 'age': 20}
res = tuple(n)
print(n, type(n), res, type(res))

